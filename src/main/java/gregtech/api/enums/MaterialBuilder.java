    public enum FuelType {

        Diesel(0),
        Gas(1),
        Thermal(2),
        SemiFluid(3),
        Plasma(4),
        Magic(5);

        final private int index;

        FuelType(int index) {
            this.index = index;
        }

        public static FuelType fromIndex(int index) {
            return switch (index) {
                case 0 -> Diesel;
                case 1 -> Gas;
                case 2 -> Thermal;
                case 3 -> SemiFluid;
                case 4 -> Plasma;
                case 5 -> Magic;
                default -> throw new IllegalStateException("Invalid FuelType: " + index);
            };
        }

        public int getIndex() {
            return index;
        }
    }

